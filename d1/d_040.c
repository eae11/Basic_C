#include <stdio.h>

int main() {
    /*
     数组指针的定义方法
     指向的数组的类型 (* 指针变量名) [指向的数组的元素个数]
     */
    int a[3][5];
    int (*p)[5];//定义一个数组指针变量p(4字节),p指向的是 整型的5个元素的数组 注意加() 不加() int *p[5] 就成了数组里面存放5个指针(20个字节)
    printf("%d\n", sizeof(p));//4
    printf("%p\n", a);//a是一维数组指针 而&a[0][0]是整型指针
    printf("%p\n", a + 1);//往下是指一个数组 相差20个字节
    p = a;
    printf("%p\n", p);
    printf("%p\n", p + 1);

    printf("%p\n", &a[0][0]);//整型指针
    printf("%p\n", &a[0][0] + 1);//相差4个字节
    return 0;
}